
/**
* op_add - add two numbers
* @a: first integer
* @b: second integer
*
* Return: a+b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtract one number from another
* @a: first number
* @b: second number
*
* Return: a-b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiply two numbers
* @a: first number
* @b: second number
*
* Return: a*b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - divide two numbers
* @a: first number
* @b: second number
*
* Return: a/b
*/
int op_div(int a, int b)
{
	return (a / b);
}


/**
* op_mod - modulus of a number with another
* @a: first integer
* @b: second integer
*
* Return: a%b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
